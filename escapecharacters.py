

# This symbol '#' is used for single line comment.
#   \n --> new line
print('This is USA.\nThe country of liberty and oppurtunities.')
#   \t --> horizantal tab
print('apple\tball\tcat')
#   \r --> carriage return
#   \b --> backspace
#   \f --> form feed
#   It forces the printer to eject the current page and to continue printing at the top of another.
#   \' --> single quote
print('It\'s raining')
#   \" --> double quote
print('\"It\'s raining\"')
#   \\ --> back slash
